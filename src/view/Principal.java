    public void Hour() {
        Calendar calendar = new GregorianCalendar();
        Date hourA = new Date();
        calendar.setTime(hourA);
        hour = calendar.get(Calendar.HOUR_OF_DAY) > 9 ? "" + calendar.get(Calendar.HOUR_OF_DAY) : "0" + calendar.get(Calendar.HOUR_OF_DAY);
        min = calendar.get(Calendar.MINUTE) > 9 ? "" + calendar.get(Calendar.MINUTE) : "0" + calendar.get(Calendar.MINUTE);
        sec = calendar.get(Calendar.SECOND) > 9 ? "" + calendar.get(Calendar.SECOND) : "0" + calendar.get(Calendar.SECOND);

    }

    public void run() {
        Thread current = Thread.currentThread();
        while (current == hilo) {
            Hour();
            lbClockStart.setText(hour + ":" + min + ":" + sec);
        }
    }

