    private void btnAceptUserUActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptUserUActionPerformed
        // TODO add your handling code here:
        if (Settings.rbInsert.isEnabled()) {
            if (txtUser.getText().isEmpty() || pswPass.getText().isEmpty() || pswPass1.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "!Por favor llene todos los campos correctamente¡");
            } else if (!pswPass.getText().equals(pswPass1.getText())) {
                JOptionPane.showMessageDialog(null, "¡La contraseña no coinside!");
            } else {
                String nameUser = txtUser.getText();
                String passUser = pswPass.getText();
                String rool = "Usuario";
                if (LoginToAdmin.vRool.equals("ROOT")) {
                    rool = "Administrador";
                }
                controller.insertAdmin(nameUser, passUser, rool);
                clean();
            }
        } else if (Settings.rbUpdate.isEnabled()) {
            if (pswPass.getText().isEmpty() || pswPass1.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "!Por favor llene todos los campos correctamente¡");
            } else if (!pswPass.getText().equals(pswPass1.getText())) {
                JOptionPane.showMessageDialog(null, "¡La contraseña no coinside!");
            } else {
                String nameUser = txtUser.getText();
                String passUser = pswPass.getText();
                controller.updateAdmin(nameUser, passUser);
            }
            clean();
        }
        table();
    }//GEN-LAST:event_btnAceptUserUActionPerformed

