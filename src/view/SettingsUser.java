    public void clean() {
        Settings.rbInsert.setEnabled(true);
        Settings.rbDelete.setEnabled(false);
        Settings.rbUpdate.setEnabled(false);

        txtUser.setText("");
        pswPass.setText("");
        pswPass1.setText("");
        txtUser.setEnabled(false);
        pswPass.setEnabled(false);
        pswPass1.setEnabled(false);
        btnAceptUserU.setEnabled(false);
        btnCancelUserU.setEnabled(false);

        jTable1.setEnabled(true);
    }

    void searchUser(String user, String pass) {
        ResultSet result = controller.login(user, pass);
        try {
            if (result.next()) {
                IdUser = result.getInt("id");
                vUser = result.getString("nombre");
                vPass = result.getString("pass");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error " + ex.getMessage());
        }
    }

